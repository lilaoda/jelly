/**
 * 关于加密填充方式：
 * Android这边加密过的数据，服务器端死活解密不了，原来android系统的RSA实现是"RSA/None/NoPadding"，
 * 而标准JDK实现是"RSA/None/PKCS1Padding" ，这造成了在android机上加密后无法在服务器上解密的原因，所以在实现的时候这个一定要注意。
 * <p/>
 * 实现分段加密：搞定了填充方式之后又自信的认为万事大吉了，可是意外还是发生了，RSA非对称加密内容长度有限制，
 * 1024位key的最多只能加密127位数据，否则就会报错(javax.crypto.IllegalBlockSizeException:
 * Data must not be longer than 117 bytes) ，　RSA 是常用的非对称加密算法。最近使用时却出现了“不正确的长度”的异常，
 * 研究发现是由于待加密的数据超长所致。RSA 算法规定：待加密的字节数不能超过密钥的长度值除以 8 再减去 11（即：KeySize / 8 - 11），
 * 而加密后得到密文的字节数，正好是密钥的长度值除以 8（即：KeySize / 8）。
 * Created by lilaoda on 2016/10/22.
 */
