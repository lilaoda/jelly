    /**
     * 需要在manifest配置相应的activity，不然旋转无效
     */
    @Override
    public void fullScreen() {
        AppCompatActivity activity = PlayUtils.getAppCompActivity(getContext());
        ViewGroup contentView = activity.getWindow().findViewById(android.R.id.content);
        LayoutParams lp = new LayoutParams(ViewGroup.LayoutParams.MATCH_PARENT, ViewGroup.LayoutParams.MATCH_PARENT, Gravity.CENTER);
        int orientation = getResources().getConfiguration().orientation;
        boolean playing = mMediaPlayer.isPlaying();
        if (playing) pause();
        removeFromParent(mContainer);
        if (orientation == ActivityInfo.SCREEN_ORIENTATION_PORTRAIT) {
            activity.setRequestedOrientation(ActivityInfo.SCREEN_ORIENTATION_LANDSCAPE);
            mContainer.setLayoutParams(lp);
            contentView.addView(mContainer);
            PlayUtils.hideActionBar(activity);
        } else {
            activity.setRequestedOrientation(ActivityInfo.SCREEN_ORIENTATION_PORTRAIT);
            addView(mContainer);
            PlayUtils.showActionBar(activity);
        }
        if (playing) start();
    }

    private void removeFromParent(View view) {
        ViewParent parent = view.getParent();
        if (parent instanceof ViewGroup) {
            ViewGroup viewGroup = (ViewGroup) parent;
            viewGroup.removeView(view);
        }
    }

    private void updateUi() {
        int status = mCurrentState;
        switch (status) {
            case STATE_PREPARING:
                showLoadingView();
                break;
            case STATE_ERROR:
                showErrorView();
                break;
            case STATE_PLAYBACK_COMPLETED:
                showCompletedView();
                break;
            case STATE_IDLE:
                showNormalView();
                break;
            default:
                showControlView();
        }
    }

    private void showLoadingView() {
        loadingView.setVisibility(VISIBLE);
        errorView.setVisibility(GONE);
        completedView.setVisibility(GONE);
        videoInfo.setVisibility(GONE);
        if (mMediaController != null) {
            mMediaController.hide();
        }
    }

    private void showErrorView() {
        errorView.setVisibility(VISIBLE);
        loadingView.setVisibility(GONE);
        completedView.setVisibility(GONE);
        videoInfo.setVisibility(GONE);
        if (mMediaController != null) {
            mMediaController.hide();
        }
    }

    private void showNormalView() {
        videoInfo.setVisibility(VISIBLE);
        loadingView.setVisibility(GONE);
        completedView.setVisibility(GONE);
        errorView.setVisibility(GONE);
        if (mMediaController != null) {
            mMediaController.hide();
        }

        int childCount = mContainer.getChildCount();
        StringBuffer stringBuffer = new StringBuffer();
        for (int i = 0; i < childCount; i++) {
            String simpleName = mContainer.getChildAt(i).getClass().getSimpleName();
            stringBuffer.append(simpleName).append("-");
        }
        Log.d(TAG, "showNormalView: "+stringBuffer.toString());
    }

